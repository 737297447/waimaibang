package com.lingdian.waimaibang.model;

public class JingxuanDialogBean {

	/** 城市名称*/
	private String cityName;
	/** 城市的地址*/
	private String cityDizhi;
	/** 玩法种类*/
	private String wanfaNum;
	/** 经度*/
	private double jingdu;
	/** 纬度*/
	private double weidu;
	public String getCityName() {
		return cityName;
	}
	public void setCityName(String cityName) {
		this.cityName = cityName;
	}
	public String getCityDizhi() {
		return cityDizhi;
	}
	public void setCityDizhi(String cityDizhi) {
		this.cityDizhi = cityDizhi;
	}
	public String getWanfaNum() {
		return wanfaNum;
	}
	public void setWanfaNum(String wanfaNum) {
		this.wanfaNum = wanfaNum;
	}
	public double getJingdu() {
		return jingdu;
	}
	public void setJingdu(double jingdu) {
		this.jingdu = jingdu;
	}
	public double getWeidu() {
		return weidu;
	}
	public void setWeidu(double weidu) {
		this.weidu = weidu;
	}
	
	
	
}
